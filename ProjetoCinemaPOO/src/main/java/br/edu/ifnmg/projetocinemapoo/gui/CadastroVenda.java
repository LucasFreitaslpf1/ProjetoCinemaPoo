    private void controleIngressos() {
        if (ingressos.isEmpty()) {
            cmbSessao.setEnabled(true);
            cmbGuiche.setEnabled(true);
        } else {
            cmbSessao.setEnabled(false);
            cmbGuiche.setEnabled(false);
        }
    }

    private void atualizarIngressos() {
        Double total = 0.0;
        for (Ingresso ingresso : ingressos) {
            total += ingresso.getPreco();
        }
        lblQuantidade.setText("Quantidade: " + ingressos.size());
        txtPrecoTotal.setText(total.toString());
    }

    protected void atualizarComboBox() {
        DefaultComboBoxModel<Sessao> ComboBoxS = new DefaultComboBoxModel<>();
        List<Sessao> sessoes = new SessaoDao().localizarTodos();
        ComboBoxS.addAll(sessoes);
        cmbSessao.setModel(ComboBoxS);

        DefaultComboBoxModel<Guiche> ComboBoxG = new DefaultComboBoxModel<>();
        List<Guiche> guiches = new GuicheDao().localizarTodos();
        ComboBoxG.addAll(guiches);
        cmbGuiche.setModel(ComboBoxG);
    }

    protected void limparCampos() {
        cmbGuiche.setSelectedIndex(-1);
        cmbSessao.setSelectedIndex(-1);
        chkMeiaEntrada.setSelected(false);
        ingressos.clear();
        atualizarIngressos();
        controleIngressos();
        txtClassificacao.setText(null);
        txtHorario.setText(null);
        txtDuracao.setText(null);
        txtPreco.setText(null);
        txtPrecoTotal.setText(null);
        txtSala.setText(null);
    }


