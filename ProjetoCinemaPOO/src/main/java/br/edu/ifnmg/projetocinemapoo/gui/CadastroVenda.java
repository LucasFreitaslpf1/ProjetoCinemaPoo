    private List<Ingresso> ingressos = new ArrayList<>();
    private void btnAdicionarIngressoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarIngressoActionPerformed
        // TODO add your handling code here:
        if (cmbGuiche.getSelectedItem() == null || cmbSessao.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Selecione o Guichê e a Sessão");
        } else {
            Ingresso i = new Ingresso();
            Sessao s = new Sessao();
            s = (Sessao) cmbSessao.getSelectedItem();
            if (chkMeiaEntrada.isSelected()) {
                i.setMeiaEntrada(true);
            } else {
                i.setMeiaEntrada(false);
            }
            i.setPreco(s.getValorSessao());
            ingressos.add(i);
            atualizarIngressos();
            controleIngressos();
        }
    }//GEN-LAST:event_btnAdicionarIngressoActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        Venda v = new Venda();
        Sessao s;

        // ASSOCIA FUNCIONARIO QUE FEZ A VENDA AO GUICHE E
        // INCREMENTA O NUMERO DE INGRESSOS VENDIDOS DA SESSAO
        s = (Sessao) cmbSessao.getSelectedItem();
        s.vendeuIngressos(ingressos.size());
        new SessaoDao().salvar(s);

        v.setQuantidade((short) ingressos.size());
        v.setGuiche((Guiche) cmbGuiche.getSelectedItem());
        v.setSessao(s);
        v.setIngressos(ingressos);
        v.setFuncionario(funcionario);
        Double total = 0.0;
        for (Ingresso ingresso : ingressos) {
            total += ingresso.getPreco();
        }
        v.setValorTotal(total);
        new VendaDao().salvar(v);
        limparCampos();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnRemoverUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverUltimoActionPerformed
        // TODO add your handling code here:
        ingressos.remove(ingressos.size() - 1);
        atualizarIngressos();
        controleIngressos();
    }//GEN-LAST:event_btnRemoverUltimoActionPerformed

    private void cmbSessaoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbSessaoItemStateChanged
        // TODO add your handling code here
        if (evt.getStateChange() == SELECTED) {
            Sessao s = (Sessao) cmbSessao.getSelectedItem();
            txtClassificacao.setText(s.getFilme()
                    .getClassificacao().getDescription().replace("_", "+"));
            txtDuracao.setText(s.getFilme().getDuracao() + " minutos");
            txtHorario.setText(s.getHorario().
                    format(DateTimeFormatter.ofPattern("dd/MM/yyy hh:mm")));
            txtPreco.setText(s.getValorSessao().toString());
            txtSala.setText(s.getSala().getTela().getDescription().replace("_", ""));
        }
    }//GEN-LAST:event_cmbSessaoItemStateChanged

